# Conditional statements
x = 10
y = 5

# if statement
if x > y:
    print(x," is greater than ",y)

# if-else statement
if x < y:
    print(x," is less than ",y)
else:
    print(x," is not less than ",y)

# if-elif-else statement
if x < y:
    print(x," is less than ",y)
elif x == y:
    print(x," is equal to ",y)
else:
    print(x," is greater than ",y)
