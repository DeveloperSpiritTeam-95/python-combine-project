# Addition (+)
result_addition = 10 + 5
print("Addition: ",result_addition)

# Subtraction (-)
result_substraction = 10 - 5
print("Substraction: ",result_substraction)

# Multiplication (*)
result_multiplication = 10 * 5
print("Multiplication:", result_multiplication)

# Division (/)
result_division = 10 / 5
print("Division:", result_division)

# Modulus (%)
result_modulus = 10 % 3
print("Modulus:", result_modulus)

# Exponentiation (**)
result_exponentiation = 2 ** 3
print("Exponentiation:", result_exponentiation)

# Floor Division (//)
result_floor_division = 10 // 3
print("Floor Division:", result_floor_division)
