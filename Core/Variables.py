x = 10
print(type(x),": ",x)

y = 3.14
print(type(y),": ",y)

name = "prabhakar"
print(type(name),": ",str(name))

numbers = [1, 2, 3, 4, 5]
print(type(numbers),": ",str(numbers))

coordinates = (3, 4)
print(type(coordinates),": ",str(coordinates))

student = {"name": "prabhakar", "age": 25}
print(type(student),": ",str(student))

unique_numbers = (1, 2, 3, 4, 5)
print(type(unique_numbers),": ",str(unique_numbers))

frozen_numbers = frozenset({1, 2, 3})
print(type(frozen_numbers),": ",str(frozen_numbers))

is_student = True
print(type(is_student),": ",str(is_student))

binary_data = b'hello'
print(type(binary_data),": ",str(binary_data))

mutable_binary_data = bytearray(b'hello')
print(type(mutable_binary_data),": ",str(mutable_binary_data))

view_memory = memoryview(b'hello')
print(type(view_memory),": ",str(view_memory))



