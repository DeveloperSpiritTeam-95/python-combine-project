x = 10  # Binary: 1010
y = 4   # Binary: 0100

# Bitwise AND
print("Bitwise AND:", x & y)

# Bitwise OR
print("Bitwise OR:", x | y)

# Bitwise XOR
print("Bitwise XOR:", x ^ y)

# Bitwise NOT
print("Bitwise NOT:", ~x)

# Left Shift
print("Left Shift:", x << 1)

# Right Shift
print("Right Shift:", x >> 1)
