name = "prabhakar naik."

# endswith() function
print(name.endswith("naik."))
print(name.endswith("naik"))

# capitalize() function
print(name.capitalize())
print(name)

# replace() function
print(name.replace("p","P"))
print(name)

# index find() function
print(name.find("k"))
print(name.find("S"))  # not valid it give -1

# count()function
print(name.count("a"))
print(name.count("naik"))

