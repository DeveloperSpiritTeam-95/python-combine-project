
# list methods
list = [20,30,40]
print(list)

# append() in list 
list.append(60)
list.append(10)
print(list)

# sort() in list
list.sort()
print(list)

# sort(reverse = True) in list
list.sort(reverse=True)
print(list)

# reverse() in list
list.reverse()
print(list)

# remove() in list base on value
list.remove(10)
print(list)

# insert() in list base on index
list.insert(0,90)
print(list)

# pop() in list base on index
list.pop(4)
print(list)
